package day1;

public class Datatype1 {

    public static void main(String[] args) {
        // 두 개의 정수 변수를 선언하고 초기화
        int num1 = 5;
        int num2 = 10;

        // 두 개의 정수 변수를 더하고 결과를 출력
        int sumInt = num1 + num2;
        System.out.println("두 정수의 합: " + sumInt);

        // 두 개의 실수 변수를 선언하고 초기화
        double double1 = 5.55;
        double double2 = 6.66;

        // 두 개의 실수 변수를 더하고 결과를 출력
        double sumDouble = double1 + double2;
        System.out.println("두 실수의 합: " + sumDouble);

        // 두 개의 문자열 변수를 선언하고 초기화
        String str1 = "Hello, ";
        String str2 = "world!";

        // 두 개의 문자열 변수를 더하고 결과를 출력
        String concatenatedStr = str1 + str2;
        System.out.println("두 문자열의 연결: " + concatenatedStr);
    }
}